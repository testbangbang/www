	public static int char2utf8(char[] src, int len, byte[] dst) {
		int res = 0, i;

		for (i = 0; i < len; i++) {
			if (src[i] >= 0x07ff) {// 3
				if (dst != null) {
					dst[res++] = (byte) (0xe0 | ((src[i] >> 12) & 0x0f));
					dst[res++] = (byte) (0x80 | ((src[i] >> 6) & 0x3f));
					dst[res++] = (byte) (0x80 | (src[i] & 0x3f));
				} else res += 3;
			} else
			if (src[i] >= 0x0080) {//2
				if (dst != null) {
					dst[res++] = (byte) (0xc0 | ((src[i] >> 6) & 0x1f));
					dst[res++] = (byte) (0x80 |  (src[i] & 0x3f));
				} else res += 2;
			} else {
				if (dst != null) {
					dst[res++] = (byte) src[i];
				} else res++;
			}
		}

		if (dst != null)
			dst[res] = 0x00;

		return res;
	}

